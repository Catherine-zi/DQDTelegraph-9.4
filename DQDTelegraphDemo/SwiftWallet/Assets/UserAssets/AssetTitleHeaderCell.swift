import UIKit

class AssetTitleHeaderCell: UITableViewCell {

    @IBOutlet weak var nameLbl: UILabel!
    @IBOutlet weak var holdLbl: UILabel!
    @IBOutlet weak var profitLbl: UILabel!
    override func awakeFromNib() {
        super.awakeFromNib()
        nameLbl.text = SWLocalizedString(key: "name")
        holdLbl.text = SWLocalizedString(key: "holdings")
        profitLbl.text = SWLocalizedString(key: "profit_24h")
    }
    
}
